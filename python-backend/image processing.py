def verify_certificate(text, user_name, known_courses):
    matched_courses = [c for c in known_courses if c in text.lower()]
    platform_found = any(p in text.lower() for p in ["coursera", "udemy", "edx", "skillshare"])
    user_verified = user_name in text

    return {
        "courses_found": matched_courses,
        "platform_verified": platform_found,
        "user_name_verified": user_verified,
        "valid_certificate": bool(matched_courses and platform_found)
    }

